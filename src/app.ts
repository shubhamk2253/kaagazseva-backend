import express, { Application } from 'express';
import cors from 'cors';
import helmet from 'helmet';
import morgan from 'morgan';
import cookieParser from 'cookie-parser';
import compression from 'compression';

import routes from './routes';

import { requestIdMiddleware } from './middleware/request-id.middleware';
import { auditMiddleware } from './middleware/audit.middleware';
import { errorMiddleware } from './middleware/error.middleware';

import {  apiLimiter } from './middleware/rateLimit.middleware';

import logger from './core/logger';

// Cron schedulers
import { startAutoEscalationJob } from './jobs/autoEscalation.job';

/**
 * KAAGAZSEVA - Express App Configuration
 */

const app: Application = express();

///////////////////////////////////////////////////////////
// SECURITY MIDDLEWARE
///////////////////////////////////////////////////////////

app.use(helmet());

///////////////////////////////////////////////////////////
// CORS
///////////////////////////////////////////////////////////

app.use(
  cors({
    origin: [
      'http://localhost:5173',
      'https://kaagazseva-frontend.onrender.com'
    ],
    credentials: true,
  })
);

///////////////////////////////////////////////////////////
// COMPRESSION
///////////////////////////////////////////////////////////

app.use(compression());

///////////////////////////////////////////////////////////
// GLOBAL RATE LIMITER
///////////////////////////////////////////////////////////

app.use(apiLimiter);

///////////////////////////////////////////////////////////
// RAZORPAY WEBHOOK RAW BODY
///////////////////////////////////////////////////////////

app.use(
  '/api/v1/payments/webhook',
  express.raw({ type: 'application/json' })
);

///////////////////////////////////////////////////////////
// BODY PARSERS
///////////////////////////////////////////////////////////

app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true }));
app.use(cookieParser());

///////////////////////////////////////////////////////////
// REQUEST TRACKING
///////////////////////////////////////////////////////////

app.use(requestIdMiddleware);

///////////////////////////////////////////////////////////
// REQUEST LOGGING
///////////////////////////////////////////////////////////

if (process.env.NODE_ENV !== 'production') {
  app.use(morgan('dev'));
}

///////////////////////////////////////////////////////////
// HEALTH CHECK
///////////////////////////////////////////////////////////

app.get('/health', (_req, res) => {
  res.json({
    status: 'ok',
    service: 'KaagazSeva Backend',
    timestamp: new Date().toISOString(),
  });
});

app.get('/', (_req, res) => {
  res.json({
    status: 'success',
    message: 'KaagazSeva Backend is Live 🚀',
  });
});

///////////////////////////////////////////////////////////
// API ROUTES
///////////////////////////////////////////////////////////

app.use('/api/v1', routes);

///////////////////////////////////////////////////////////
// AUDIT LOGGER
///////////////////////////////////////////////////////////

app.use(auditMiddleware);

///////////////////////////////////////////////////////////
// ERROR HANDLER
///////////////////////////////////////////////////////////

app.use(errorMiddleware);

///////////////////////////////////////////////////////////
// START CRON JOBS
///////////////////////////////////////////////////////////

startAutoEscalationJob();

///////////////////////////////////////////////////////////
// PROCESS SAFETY HANDLERS
///////////////////////////////////////////////////////////

process.on('unhandledRejection', (reason) => {

  logger.error({
    event: 'UNHANDLED_REJECTION',
    reason
  });

});

process.on('uncaughtException', (error) => {

  logger.error({
    event: 'UNCAUGHT_EXCEPTION',
    error
  });

  process.exit(1);

});

export default app;